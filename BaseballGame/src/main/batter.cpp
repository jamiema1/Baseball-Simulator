bool Batter::operator==(Batter obj) {
	for (int i = 0; i < 5; i++) {
		if (this->stringStatSelector(i) != obj.stringStatSelector(i)) {
			return false;
		}
	}
	for (int i = 0; i < 14; i++) {
		if (this->stats[i] != obj.stats[i]) {
			return false;
		}
	}
	return true;
}

bool Batter::operator!=(Batter obj) {
	return !(operator==(obj));
}

